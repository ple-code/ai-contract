async def seed():
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async with async_session() as db:
        existing = await db.execute(select(LegalArticle).limit(1))
        if existing.scalar_one_or_none():
            print("→ 法律条文已存在，跳过")
            return

        for a in ARTICLES:
            db.add(LegalArticle(**a))
        await db.commit()
        print(f"✓ 导入 {len(ARTICLES)} 条法律条文")


if __name__ == "__main__":
    asyncio.run(seed())
